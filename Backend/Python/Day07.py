#1. Write a program to convert temperature from Celsius to Fahrenheit.
cel=float(input("Enter Celsius: "))
far=(cel*9/5)+32
print("Fahrenheit value is",far)

#2. Write a program to convert temperature from Fahrenheit to Celsius
faren=float(input("Enter Fahrenheit: "))
celcius=(faren-32)*5/9
print("Celsius value is",celcius)